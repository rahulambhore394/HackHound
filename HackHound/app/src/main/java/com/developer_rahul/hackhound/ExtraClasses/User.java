package com.developer_rahul.hackhound.ExtraClasses;

public class User {
    public String name, email, mobile, username, password;

    public User() {}

    public User(String name, String email, String mobile, String username, String password) {
        this.name = name;
        this.email = email;
        this.mobile = mobile;
        this.username = username;
        this.password = password;
    }
}
